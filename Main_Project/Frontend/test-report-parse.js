// Sample report from backend
const sampleReport = `# Financial Portfolio Report

## User Profile Summary
- **Goal:** Analyze the performance of ACB over the last 20 days
- **Risk Tolerance:** moderate
- **Time Horizon:** short-term
- **Preferences:** Technical analysis of ACB stock

## Recent Market News Context
{'query': "Recent market news", 'results': [{'title': 'ACB Analysis', 'score': 0.55}]}

## Proposed Portfolio Allocation
| Asset | Weight |
|-------|--------|
| ACB | 100.00% |

## Portfolio Performance Metrics
| Metric | Value |
|--------|-------|
| Total Return | -1.89% |
| Annualized Return | -27.48% |
| Sharpe Ratio | -1.39 |

## LLM Commentary & Reasoning
The allocation is concentrated entirely in ACB to align with your preference.
Historical performance over the period shows challenges.

## Validation Status
- **Status:** PASS

---
**Disclaimer:** This report is generated by an AI system for informational purposes only.`;

// Test parsing
const parts = sampleReport.split('##');
const sections = {};

parts.forEach((part) => {
  const lines = part.trim().split('\n');
  if (lines.length > 0) {
    const title = lines[0].trim();
    const body = lines.slice(1).join('\n').trim();
    sections[title] = body;
  }
});

console.log('Parsed sections:', Object.keys(sections));
console.log(
  'Portfolio Allocation content:',
  sections['Proposed Portfolio Allocation']
);

// Test metric extraction
const metricsText = sections['Portfolio Performance Metrics'] || '';
const metrics = [];
const lines = metricsText.split('\n');

lines.forEach((line) => {
  if (line.includes('|') && !line.includes('---')) {
    const parts = line.split('|').filter((p) => p.trim());
    if (parts.length >= 2) {
      const label = parts[0].trim().replace(/\*\*/g, '');
      const value = parts[1].trim();

      if (label && value) {
        metrics.push({ label, value });
      }
    }
  }
});

console.log('Extracted metrics:', metrics);
